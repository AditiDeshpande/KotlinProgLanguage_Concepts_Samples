// .. operator overloading for rangeto
for(a in 1..100){
    println(a)
}

val numbers = 1..5

for(a in numbers){
    print(a)
}

for(a in 20 downTo 11){
    println(a)
}

for(b in 40 downTo 30 step 5){
    print(b)
}

val capitals = listOf("Delhi" , "Berlin" ,"London" , "Cairo" , "Washington DC")

for(capital in capitals){
    println(capital)
}

